def render_document(pages: list[list[str]]) -> str:
    lines = []
    lines.append("=" * LINE_WIDTH)
    lines.append("  软件著作权登记 — 文档鉴别材料（设计/使用说明书）")
    lines.append("  【请将下文复制到 Word，设置页眉页脚后导出 PDF】")
    lines.append("=" * LINE_WIDTH)
    lines.append("")
    lines.append("Word 排版提示：")
    lines.append("  1. 字体：宋体小四（12pt），行距固定值 20 磅")
    lines.append("  2. 页眉：左侧/software全称+版本号；右上角插入页码域")
    lines.append("  3. 每页正文区不少于 30 行（不含页眉页脚）")
    lines.append("  4. 将【截图位置】方框替换为实际软件运行截图")
    lines.append("  5. 交存：全文≥60页时取前30页+后30页；不足60页交全文")
    lines.append("")
    lines.append("=" * LINE_WIDTH)
    lines.append("")

    for i, body in enumerate(pages, 1):
        lines.extend(page_header(i))
        lines.extend(body)
        lines.append("")
        lines.append("")

    lines.append("=" * LINE_WIDTH)
    lines.append(f"  全文共 {len(pages)} 页")
    lines.append("  交存页范围：第 1-30 页" + (
        f" + 第 {len(pages)-29}-{len(pages)} 页" if len(pages) >= 60 else "（全文不足60页，交存全部）"
    ))
    lines.append("=" * LINE_WIDTH)
    return "\n".join(lines)


