LINE_WIDTH = 64
SEP = "─" * LINE_WIDTH


def page_header(page_num: int) -> list[str]:
    title = f"{SOFTWARE_NAME}  {VERSION}"
    right = f"第 {page_num} 页"
    pad = LINE_WIDTH - len(title) - len(right)
    if pad < 2:
        pad = 2
    return [SEP, title + " " * pad + right, SEP, ""]


def screenshot_block(fig_no: int, caption: str, lines: int = 8) -> list[str]:
    block = [
        f"┌{'─' * (LINE_WIDTH - 2)}┐",
        f"│  【截图位置 图{fig_no}】{' ' * (LINE_WIDTH - 16 - len(str(fig_no)))}│",
    ]
    for _ in range(lines - 2):
        block.append(f"│{' ' * (LINE_WIDTH - 2)}│")
    block.append(f"└{'─' * (LINE_WIDTH - 2)}┘")
    block.append(f"图{fig_no}  {caption}")
    block.append("")
    return block


def pad_page(lines: list[str], min_lines: int = 30) -> list[str]:
    """确保正文区（不含页眉3行）不少于 min_lines 行"""
    content = [l for l in lines if l is not None]
    while len(content) < min_lines:
        content.append("")
    return content


